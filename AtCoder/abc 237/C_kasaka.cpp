void solve()
{
    string s;
    cin >> s;

    int n = s.size();

    int j = n - 1;
    int i = 0;

    int aSuf = 0;
    int aPre = 0;

    while (j >= 0 and s[j] == 'a')
        aSuf++, j--;

    while (i < n and s[i] == 'a')
        aPre++, i++;

    string a = s.substr(i, j - i + 1);
    string b = a;
    reverse(all(b));

    if (aSuf >= aPre and a == b)
        cout << "Yes";
    else
        cout << "No";
}
