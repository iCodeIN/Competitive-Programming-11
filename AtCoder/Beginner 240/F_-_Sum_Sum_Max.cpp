int f(int a, int n, int d)
{
    int ans = n * ((2ll * a) + (n - 1) * d);

    return ans / 2ll;
}

class Solution
{

public:
    static void solve()
    {
        int n;
        cin >> n;

        int m;
        cin >> m;

        v<pii> C(n);

        for (auto &c : C)
            cin >> c.ff >> c.ss;

        int prefixSum = 0;

        int p = 0;

        int ans = C[0].ff;

        for (int i = 0; i < n; i++)
        {
            int termsHere = C[i].ss;

            int cur = prefixSum + p + C[i].ff;

            ans = max(ans, cur);

            prefixSum += f(p + C[i].ff, termsHere, C[i].ff);

            ans = max(ans, prefixSum);

            p += (C[i].ff * termsHere);
        }

        cout << ans << endl;
    }
};
