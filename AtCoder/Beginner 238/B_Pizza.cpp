// Solution
// // //  // // //  // // //  // // // // // //  // // // // // //  // // //
class Solution
{

public:
    static void solve()
    {
        int n;
        cin >> n;
        v<int> A(n);

        for (int &x : A)
            cin >> x;

        v<int> cut(360, 0);

        int cur = 0;
        for (int &x : A)
        {
            cur += x;
            cur %= 360;
            cut[cur] = 1;
        }

        int ans = 0;
        int prev = 0;
        for (int i = 0; i < 360; i++)
        {
            if (cut[i] == 1)
            {
                ans = max(ans, i - prev);
                prev = i;
            }
        }
        prev = 360;
        for (int i = 359; i >= 0; i--)
        {
            if (cut[i] == 1)
            {
                ans = max(ans, prev - i);
                prev = i;
            }
        }
        cout << ans << endl;
    }
};
