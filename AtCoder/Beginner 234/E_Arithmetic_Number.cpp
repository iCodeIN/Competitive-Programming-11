set<int> st;

void add(int a, int d)
{
    string s = "";

    while (a >= 0 and a <= 9 and s.length() < 18)
    {
        s += (char)(a + '0');
        a += d;
        st.insert(stoll(s));
    }
}
void solve()
{
    st.insert(0);
    for (int start = 1; start <= 9; start++)
    {
        for (int d = -9; d <= 9; d++)
        {
            add(start, d);
        }
    }
    int n;
    cin >> n;
    cout << *st.lower_bound(n) << endl;
}
