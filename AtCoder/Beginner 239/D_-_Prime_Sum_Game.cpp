// Solution
// // //  // // //  // // //  // // // // // //  // // // // // //  // // //
class Solution
{

    static bool isPrime(int n)
    {
        int c = 0;
        for (int i = 1; i <= n; i++)
            if (n % i == 0)
                c++;
        return c == 2;
    }

public:
    static void solve()
    {
        int a, b, c, d;
        cin >> a >> b >> c >> d;

        string ans = "Aoki";
        for (int i = a; i <= b; i++)
        {
            bool all = true;
            for (int j = c; j <= d; j++)
            {
                if (isPrime(i + j))
                    all = false;
            }
            if (all)
                ans = "Takahashi";
        }

        cout << ans << endl;
    }
};
