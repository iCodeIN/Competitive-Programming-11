void solve()
{
    int n;
    cin >> n;

    v<int> A(n);
    int ans = 0;

    int F[3] = {0};
    int mx = 0;
    for (int i = 0; i < n; i++)
    {
        cin >> A[i];
        F[A[i] % 3]++;
        mx = max(A[i], mx);
    }

    // with 3
    int ans1 = mx / 3 + (F[1] > 0) + (F[2] > 0);

    // with 2
    int ans2 = mx / 2 + (F[1] > 0);

    // with 1
    int ans3 = mx;

    cout << min({ans1, ans2, ans3}) << endl;
}
