void solve()
{
    string s;
    cin >> s;
    int n = s.size();
    int zero = 0;
    int one = 0;
    int ans = 0;
    for (int i = 0; i < n; i++)
    {
        if (s[i] == '0')
            zero++;
        else
            one++;

        if (zero == one)
            ans = max(ans, zero - 1);
        else
            ans = max(ans, min(zero, one));
    }
    cout << ans << endl;
}
