void solve()
{
    string s;
    cin >> s;

    int n = s.length();
    int m = stoll(s) % 7;
    int lastDigit = (s[n - 1] - '0') % 10;

    if (lastDigit - m >= 0)
    {
        s[n - 1] = (lastDigit - m) + '0';
    }
    else
    {
        s[n - 1] = (lastDigit + (7 - m)) + '0';
    }

    cout << s << endl;
}
