// // //  // // //  // // //  // // // // // //  // // // // // //  // // //
void solve()
{
    int n;
    cin >> n;
    string s;
    cin >> s;

    v<int> A(n);
    int ans = 0;

    for (int i = 0; i < n; i++)
        A[i] = s[i] - '0';

    for (int i = 1; i < n; i++)
        A[i] += A[i - 1];

    map<int, int> mp;
    for (int r = 0; r < n; r++)
    {
        ans += mp[A[r] - r];
        mp[A[r] - r]++;
    }

    for (int r = 0; r < n; r++)
        ans += (A[r] == r + 1);
    cout << ans << endl;
}
