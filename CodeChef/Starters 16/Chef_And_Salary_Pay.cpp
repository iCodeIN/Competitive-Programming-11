void solve()
{
    int x, y;
    cin >> x >> y;

    string s;
    cin >> s;

    int n = s.length();
    int mx = 0;
    int count = 0;
    for (int i = 0; i < n;)
    {
        if (s[i] == '0')
        {
            i++;
            continue;
        }
        int cur = 0;
        while (i < n and s[i] == '1')
            i++, count++, cur++;

        mx = max(mx, cur);
    }
    int ans = (count * x) + mx * y;

    cout << ans << endl;
}

