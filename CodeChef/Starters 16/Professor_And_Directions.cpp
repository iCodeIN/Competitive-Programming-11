void solve()
{
    int n;
    cin >> n;
    string s;
    cin >> s;

    map<int, int> mp;

    mp[0]++;

    int pre = 0;
    bool ans = false;
    for (int i = 0; i < n; i++)
    {
        // pre - 2 or pre + 2

        if (mp.count(pre - 2) or mp.count(pre + 2))
            ans = true;

        if (s[i] == 'R')
            pre++;
        else
            pre--;
        mp[pre]++;
    }

    if (ans)
        cout << "YES";
    else
        cout << "NO";
    cout << endl;
}

