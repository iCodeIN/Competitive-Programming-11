int dp[1001];

int solve(int n, int cut)
{

    int &ans = dp[n];
    
    if (ans != -1)
        return ans;

    if (n & 1)
        return 0;

    if (n == 0)
        return 1;

    ans = 0;

    for (int i = 2; i <= cut and i <= n; i += 2)
        ans = (ans + solve(n - i, cut)) % mod;

    return ans;
}

void solve()
{
    int n, cut;
    cin >> n >> cut;

    memset(dp, -1, sizeof(dp));
    int ans = solve(n, cut);

    cout << ans << endl;
}

