// // //  // // //  // // //  // // // // // //  // // // // // //  // // //
void solve()
{

    int n;
    cin >> n;

    if (n == 2)
        cout << "NO";
    else
    {
        cout << "YES\n";
        v<int> A(n);

        int m = (n + 1) / 2;
        int j = 0;
        for (int i = n; i > m; i--)
            A[j++] = i;
        if (n % 2 == 0)
            swap(A[j - 2], A[j - 1]);
        for (int i = 1; i <= m; i++)
            A[j++] = i;

        for (int &x : A)
            cout << x << " ";
    }
    cout << endl;
}
