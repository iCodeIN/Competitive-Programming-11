v<int> A(1000);

int bitCount(int x)
{
    int ans = 0;
    while (x)
    {
        ans += (x & 1);
        x >>= 1;
    }
    return ans;
}
void solve()
{
    int n;
    cin >> n;

    for (int i = 0; i < n; i++)
        cout << A[i] << " ";

    cout << endl;
}
