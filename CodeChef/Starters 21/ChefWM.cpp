void solve()
{
    int n;
    cin >> n;

    int m;
    cin >> m;
    int ans = 0;
    for (int i = 2; i * i <= m; i++)
    {
        ans += (m % i == 0);
        while (m % i == 0)
            m /= i;
    }
    if (m != 1)
        ans++;
    bool f = false;
    for (int i = min(ans, n); i >= 1; i--)
    {
        if (n % i == 0)
        {
            ans = i;
            f = true;
            break;
        }
    }
    if (!f)
        ans = 0;
    cout << ans << endl;
}
