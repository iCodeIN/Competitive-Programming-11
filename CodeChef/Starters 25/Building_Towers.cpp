// Solution
// // //  // // //  // // //  // // // // // //  // // // // // //  // // //
class Solution
{
    static int solve(int x, int op, int inv, int current)
    {

        if (op == 0)
            return 0;

        if (inv >= x)
            return op;

        int used = min(x - current, inv);

        if (current + used == x)
            return 1 + solve(x, op - 1, inv + current, 0);

        return solve(x, op - 1, inv + current, current + used);
    }

public:
    static void solve()
    {
        int x, op;
        cin >> x >> op;

        int ans = solve(x, op, 1, 0);

        cout << ans << endl;
    }
};
