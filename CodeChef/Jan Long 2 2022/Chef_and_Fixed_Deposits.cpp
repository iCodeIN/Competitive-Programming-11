#define v vector
// // //  // // //  // // //  // // // // // //  // // // // // //  // // //
void solve()
{
    int n;
    cin >> n;

    int x;
    cin >> x;

    v<int> A(n);
    int ans = 0;

    for (int i = 0; i < n; i++)
        cin >> A[i];

    sort(all(A));
    reverse(all(A));
    int i = 0;

    while (x > 0 and i < n)
    {
        x -= A[i];
        i++;
        ans++;
    }

    cout << ((x > 0) ? -1 : ans) << endl;
}
