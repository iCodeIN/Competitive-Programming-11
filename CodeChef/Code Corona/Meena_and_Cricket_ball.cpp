// // //  // // //  // // //  // // // // // //  // // // // // //  // // //
void solve()
{
    int x;
    cin >> x;
    int n;
    cin >> n;
    int b;
    cin >> b;
    swap(x, b);
    v<int> A(n);
    for (int i = 0; i < n; i++)
        cin >> A[i];

    sort(all(A));
    int ans = 0;
    b -= x;
    int i = 0;
    while (i < n and A[i] <= b)
    {
        ans++;
        b -= A[i];
        i++;
    }
    cout << ans << endl;
}
