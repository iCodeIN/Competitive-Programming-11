void solve()
{
    int n, k;
    cin >> n >> k;
    vector<int> a(n + 1);
    for (int i = 1; i <= n; i++)
    {
        cin >> a[i];
    }
    int cnt = 0;
    if (a[k])
        cnt++;
    int i = k - 1, j = k + 1;
    while (i > 0 || j <= n)
    {
        if (i > 0 && j <= n)
        {
            if (a[i] + a[j] == 2)
                cnt += 2;
        }
        else
        {
            if (i > 0 && a[i])
                cnt++;
            if (j <= n && a[j])
                cnt++;
        }
        i--;
        j++;
    }
    cout << cnt << endl;
}
