bool check(string &s, string p)
{
    int n = s.size();

    for (int i = 0; i < n; i++)
        if (s.substr(i, 2) == p)
            return true;

    return false;
}

void solve()
{
    string s;
    cin >> s;

    bool ans = check(s, "10");
    ans |= check(s, "11");

    if (ans)
        cout << "Yes";
    else
        cout << "No";

    cout << endl;
}
