int N = 1e8;
vector<bool> isPrime(N + 1, true);

void solve()
{
    isPrime[0] = isPrime[1] = false;
    for (int i = 2; i * i <= N; i++)
    {
        if (isPrime[i])
            for (int j = i * i; j <= N; j += i)
                isPrime[j] = false;
    }
    int count = 0;
    for (int i = 1; i <= N; i++)
    {
        if (isPrime[i])
        {
            ++count;
            if (count % 100 == 1)
                cout << i << endl;
        }
    }

    return;
}

