void solve()
{
    int x, y;
    cin >> x >> y;
    int ans = 0;
    for (int k = 1; k * k < x and k < y; k++)
    {

        int start = (k + 1) * k + k;
        int end = min((y * k + k), x);

        if (end % k != start % k)
        {
            end -= end % k;
            end += start % k;
        }
        ans += max((end - start) / k + 1, 0ll);
    }
    cout << ans << endl;
}

